//"ifternario"
//Função principal que executa o programa.
fun main() {
//Declaração da função 'max', que recebe dois parâmetros Int e retorna o maior valor entre eles usando um if-else em uma única linha.
    fun max(a: Int, b: Int) = if (a > b) a else b

//Chamada da função 'max' com os argumentos 99 e -42, imprimindo o maior valor (99).
    println(max(99, -42))

}